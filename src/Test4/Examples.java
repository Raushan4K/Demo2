package Test4;

public class Examples {

	public static void main(String[] args) 
	{
		int x=12;
		int y=-12;
	    System.out.println("========For positive number=====");
		System.out.println(x<<1);  //24
		System.out.println(x>>1);  //6
		System.out.println(x>>>1);  //6
		System.out.println("======For negative number====");
		System.out.println(y<<1);  //-24
		System.out.println(y>>1);   //-6
		System.out.println(y>>>1);   //2147483642
       
	}

}
